                        <p>PT. Telkom Akses (PTTA) merupakan anak perusahaan PT Telekomunikasi Indonesia, Tbk (Telkom) yang sahamnya dimiliki sepenuhnya oleh Telkom. PTTA bergerak dalam bisnis penyediaan layanan konstruksi dan pengelolaan infrastruktur jaringan.</p>
                        <p><img src="<?= base_url()?>assets/thumbnails/telkom_akses1.jpg"></p>
                        <p>Pendirian PTTA merupakan bagian dari komitmen Telkom untuk terus melakukan pengembangan jaringan broadband untuk menghadirkan akses informasi dan komunikasi tanpa batas bagi seluruh masyarakat indonesia. Telkom berupaya menghadirkan koneksi internet berkualitas dan terjangkau untuk meningkatkan kualitas sumber daya manusia sehingga mampu bersaing di level dunia. Saat ini Telkom tengah membangun jaringan backbone berbasis Serat Optik maupun Internet Protocol (IP) dengan menggelar 30 node terra router dan sekitar 75.000 Km kabel Serat Optik. Pembangunan kabel serat optik merupakan bagian dari program Indonesia Digital Network (IDN) 2015. Sebagai bagian dari strategi untuk mengoptimalkan layanan nya, Telkom mendirikan PT. Telkom Akses.

						Kehadiran PTTA diharapkan akan mendorong pertumbuhan jaringan akses broadband di indonesia. Selain Instalasi jaringan akses broadband, layanan lain yang diberikan oleh PT. Telkom Akses adalah Network Terminal Equipment (NTE), serta Jasa Pengelolaan Operasi dan Pemeliharaan (O&M â€“ Operation & Maintenance) jaringan Akses Broadband.</p>
                        
                       
